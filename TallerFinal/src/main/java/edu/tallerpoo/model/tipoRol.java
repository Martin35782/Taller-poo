package edu.tallerpoo.model;

/**
 * Enumeración que representa los roles posibles de los funcionarios en una organización.
 */
public enum tipoRol {
    /**
     * Rol de Administrador Asignador.
     */
    AdminAsignador,
    
    /**
     * Rol de Administrador General.
     */
    Administrador,
    
    /**
     * Rol de Médico.
     */
    Medico,
    
    /**
     * Rol de Enfermería.
     */
    Enfermeria,
    
    /**
     * Rol de Administrativo.
     */
    Administrativo;
}