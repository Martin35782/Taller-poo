    /**
     * al precionar el boton "agregar" Este metodo cierra la pantalla actual y agrega un estudio clinico
     * @param evt {@link java.awt.event.ActionEvent}
     */
    private void agregarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarButtonActionPerformed

        String id=idEstudio.getText();
        Date fechae=fechaEstudio.getDate();
        String fecha=Utilidades.convertDate(fechae);
        Date hora=new Date();
        String Hora=Utilidades.convertHora(hora);
        String tipo=tipoEstudio.getText();
        String informe=informeEstudio.getText();
       
        
        String [] ArrayDatos={id, fecha, Hora, tipo, informe};
        LogicaEstudioMedico est=new LogicaEstudioMedico();
        if (fechae.after(hora)){
            JOptionPane.showMessageDialog(this, "Ingrese una fecha menor a la actual", "", JOptionPane.ERROR_MESSAGE);
        }else{
        int opcion = JOptionPane.showOptionDialog( this,   "¿Está seguro que desea agregar el estudio?", "",  JOptionPane.YES_NO_OPTION,   JOptionPane.WARNING_MESSAGE, null, 
        new Object[]{"Sí", "No"}, 
         "Sí"); 
            if (opcion == JOptionPane.YES_OPTION) {
            est.escribirCSVEstudioMedico(ArrayDatos);
            this.dispose();
        }else if (opcion == JOptionPane.NO_OPTION) {
        }
        }
        
        
    }//GEN-LAST:event_agregarButtonActionPerformed

