    /**
     * Cuando se preciona el botonm "registrar" se crea una nueva historia clinica con los datos que se ingresaron en la pantalla
     * 
     * @param evt 
     */
    private void registrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarButtonActionPerformed
        String diagnostico=diagnosticoTxt.getText();
        String id=idTxt.getText();
        String diagnosticoClinico=diagnosticoClinicoTxt.getText();
        String lugar=lugarCombo.getSelectedItem().toString();
        Date f=new Date(); 
        String fecha= Utilidades.convertDate(f);
        String hora= Utilidades.convertHora(f);
        LogicaRegistro r=new LogicaRegistro();
        
        String [] ArrayDatos={dniTxt.getText(),nombreTxt.getText(),apellidoTxt.getText(),id,fecha, hora, Integer.toString(this.medic.getDocumento()), diagnostico, lugar, diagnosticoClinico};
        LogicaHistoriaClinica hc=new LogicaHistoriaClinica();
        int opcion = JOptionPane.showOptionDialog( this,   "¿Está seguro que desea agregar agregar Historia Clinica?", "HISTORIA CLINICA",  JOptionPane.YES_NO_OPTION,   JOptionPane.WARNING_MESSAGE, null, 
        new Object[]{"Sí", "No"}, 
         "Sí"); 
            if (opcion == JOptionPane.YES_OPTION) {
            hc.escribirCSVHisotriaClinica(ArrayDatos);
            try {
                try {
                    r.FinAtencion(Integer.parseInt(dniTxt.getText()));
                } catch (CsvValidationException ex) {
                    Logger.getLogger(NuevaHistoriaClinica.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (IOException ex) {
                Logger.getLogger(NuevaHistoriaClinica.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
        }else if (opcion == JOptionPane.NO_OPTION) {
        }
            
        JOptionPane.showMessageDialog(this, "Se han registrado los datos de la nueva Historia Clincia.", "", JOptionPane.WARNING_MESSAGE);
        this.dispose();
    }//GEN-LAST:event_registrarButtonActionPerformed

