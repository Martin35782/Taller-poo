    /**
     * al precionar el boton "Consultar" Este metodo realiza la consulta de los pacientes atendidios en un rango de
     * fechas y edades, luego los muestra por pantalla
     * @param evt 
     */
    private void btnConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultaActionPerformed
        LogicaEstadisticas logica = new LogicaEstadisticas();
        Date f1 = fecha1.getDate();
        Date f2 = fecha2.getDate();
        int edad1 = Integer.parseInt(edad1TextField.getText());
        int edad2 = Integer.parseInt(edad2TextField.getText());
        
        DefaultTableModel model = logica.EstadisticaEdadYFecha(f1, f2, edad1, edad2);
        jTable1.setModel(model);;
        
    }//GEN-LAST:event_btnConsultaActionPerformed

