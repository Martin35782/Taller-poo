    /**
     * al precionar el boton "Ver Estudios Clinicos" Este metodo cierra la pantalla actual y abre la que muestra los estudios clinicos
     * de un paciente
     * @param evt 
     */
    private void estufiosClinicosButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_estufiosClinicosButtonActionPerformed
        this.dispose();
        EstudiosClinicos n1 = new EstudiosClinicos();
        LogicaEstudioMedico le=new LogicaEstudioMedico();
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        String [] array=new String[model.getColumnCount()];
        for (int i=0; i<model.getColumnCount(); i++){
          array[i]=model.getValueAt(jTable1.getSelectedRow(), i).toString();
        }
        le.getIDHistoria(array);
        n1.setVisible(true);
        n1.setLocationRelativeTo(null);
    }//GEN-LAST:event_estufiosClinicosButtonActionPerformed


    public void actualizar(){   
        LogicaHistoriaClinica hc=new LogicaHistoriaClinica();
        ArrayList<HistoriaClinica>hc2=hc.leerHistoriaClinica();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        Utilidades u=new Utilidades();
        for(HistoriaClinica p : hc2){
            if((p.getPaciente().getDocumento()==Integer.parseInt(dni.getText()))){
            String[] array = {Integer.toString(p.getId()),Integer.toString(p.getMedico().getDocumento()),p.getDiagnostico(),Utilidades.getAtencionSTR(p.getLugarAtencion()),p.getDiagnosticoClinico(),Utilidades.convertDate(p.getFecha())};
            model.addRow(array); 
            }
            }
    }

    

