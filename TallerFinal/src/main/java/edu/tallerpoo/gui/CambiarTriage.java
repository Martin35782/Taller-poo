    private int sumador(int x){
        LogicaTriage lt=new LogicaTriage();
        int suma=(Integer.parseInt(lt.nivel(idLabel.getText()))-(x));
        return suma;
    }
    
    
    
    
    /**
     * al precionar el boton "Confirmar Cambio" Este metodo actualiza en triage del paciente y cierra la pantalla actual
     * 
     * @param evt 
     */
    private void ConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarActionPerformed
        LogicaTriage lt=new LogicaTriage();
        int z;
        if (cambio<0){
            z=(cambio*-1);
        }else{
            z=cambio;
        }
        System.out.println(cambio);
        System.out.println(z);
        int nivelviejo=Integer.parseInt(lt.nivel(idLabel.getText()));
        System.out.println(nivelviejo);
        
        if (z>2){
            JOptionPane.showMessageDialog(this, "Solo pueden haber dos niveles de diferencia en el cambio", "", JOptionPane.ERROR_MESSAGE);
        }else{
        int opcion = JOptionPane.showOptionDialog( this,   "¿Está seguro que desea cambiar el triage?", "",  JOptionPane.YES_NO_OPTION,   JOptionPane.WARNING_MESSAGE, null, 
        new Object[]{"Sí", "No"}, 
         "Sí"); 
            if (opcion == JOptionPane.YES_OPTION) {
        lt.cambiarTriage(Integer.parseInt(idLabel.getText()), motivoText.getText(), Niveles.getSelectedItem().toString());
        JOptionPane.showMessageDialog(this, "Triage cambiado con exito", "", JOptionPane.WARNING_MESSAGE);
        this.dispose();
        }else if (opcion == JOptionPane.NO_OPTION) {
        }
      }                                         
        
    }//GEN-LAST:event_ConfirmarActionPerformed

