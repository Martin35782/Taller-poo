    /**
     * Este metodo muestra el color, urgencia y el tiempo estimado de anencion en base a un nivel seleccionado
     * 
     * @param evt 
     */
    private void datosNuevosTablaPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_datosNuevosTablaPropertyChange
        try{
            String a=Niveles.getSelectedItem().toString();
            switch (a){
                case "1" -> {
                    colorBarra.setBackground(Color.red);
                    cambio=sumador(Integer.parseInt(Niveles.getSelectedItem().toString()));
                    datosNuevosTabla.setValueAt("ROJO",0,1);
                    datosNuevosTabla.setValueAt("RIESGO VITAL INMEDIATO",0,2);
                    datosNuevosTabla.setValueAt("ATENCION INMEDIATA",0,3);
                }
                case "2" -> {
                    colorBarra.setBackground(Color.orange);
                    cambio=sumador(Integer.parseInt(Niveles.getSelectedItem().toString()));
                    datosNuevosTabla.setValueAt("NARANJA",0,1);
                    datosNuevosTabla.setValueAt("MUY URGENTE",0,2);
                    datosNuevosTabla.setValueAt("10-15 MIN.",0,3);
                }
                case "3" -> {
                    colorBarra.setBackground(Color.yellow);
                    cambio=sumador(Integer.parseInt(Niveles.getSelectedItem().toString()));
                    datosNuevosTabla.setValueAt("AMARILLO",0,1);
                    datosNuevosTabla.setValueAt("URGENTE",0,2);
                    datosNuevosTabla.setValueAt("60 MIN.",0,3);
                }
                case "4" -> {
                    colorBarra.setBackground(Color.green);
                    cambio=sumador(Integer.parseInt(Niveles.getSelectedItem().toString()));
                    datosNuevosTabla.setValueAt("VERDE",0,1);
                    datosNuevosTabla.setValueAt("NORMAL",0,2);
                    datosNuevosTabla.setValueAt("2 HORAS",0,3);
                }
                case "5" -> {
                    colorBarra.setBackground(Color.blue);
                   cambio=sumador(Integer.parseInt(Niveles.getSelectedItem().toString()));
                    datosNuevosTabla.setValueAt("AZUL",0,1);
                    datosNuevosTabla.setValueAt("NO URGENTE",0,2);
                    datosNuevosTabla.setValueAt("4 HORAS",0,3);
                }
            }
        }
        catch (Exception e){
            System.out.println("Error");
        }
    }//GEN-LAST:event_datosNuevosTablaPropertyChange
