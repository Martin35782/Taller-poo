    /**
     * Cuando se preciona el boton "confirmar" se verifican los datos de logueo en la base de datos para ver si estos son validos o no
     * 
     * @param evt 
     */
    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        logicaLogin login = new logicaLogin();
        String password = new String(txtContrasena.getPassword());
        Funcionario funcio = new Funcionario();
        
        try {
            funcio = login.log(txtUsuario.getText(), password);
        } catch (IOException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (CsvValidationException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(funcio == null){
            JOptionPane.showMessageDialog(this, "Usuario o Contrasena Incorrectos.", "", JOptionPane.WARNING_MESSAGE);
        }
        else{

            switch(funcio.getRol()){
                case Medico:
                    this.dispose();
                    MedicoGUI m = new MedicoGUI();
                    m.setData(funcio);
                    m.setVisible(true);
                    m.setLocationRelativeTo(null);
                    break;
                case AdminAsignador:
                    this.dispose();
                    Administrador ad = new Administrador();
                    ad.setData(funcio, true);
                    ad.setVisible(true);
                    ad.setLocationRelativeTo(null);
                    break;
                case Administrador:
                    this.dispose();
                    Administrador admin = new Administrador();
                    admin.setData(funcio, false);
                    admin.setVisible(true);
                    admin.setLocationRelativeTo(null);
                    break;
                case Enfermeria:
                    this.dispose();
                    Enfermeria enfer = new Enfermeria();
                    enfer.setData(funcio);
                    enfer.setVisible(true);
                    enfer.setLocationRelativeTo(null);
                case Administrativo:
                    LogicaAdministrativos logic = new LogicaAdministrativos();
                {
                    try {
                        Administrativo administrativo = logic.setAdministrativo(funcio);
                        System.out.println(administrativo.getSector().getId());
                        switch(administrativo.getSector().getId()){
                            case 7:
                                this.dispose();
                                Gestores ges = new Gestores();
                                ges.setData(administrativo);
                                ges.setVisible(true);
                                ges.setLocationRelativeTo(null);
                                break;
                            case 1:
                                this.dispose();
                                ConsultaPaciente consul = new ConsultaPaciente();
                                consul.setData(administrativo);
                                consul.setVisible(true);
                                consul.setLocationRelativeTo(null);
                                break;
                        }
                    } catch (IOException ex) {
                        Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (CsvValidationException ex) {
                        Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

                    
            }
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

