    /**
     * al precionar el boton "confirmar" Este metodo agrega un funcionario al registro y cierra la pantalla actual
     * @param evt 
     */
    private void confirmarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarButtonActionPerformed
        LogicaAdminSistema logicaAdmin = new LogicaAdminSistema();
        SimpleDateFormat formatoFecha = new SimpleDateFormat("dd-MM-yyyy");
        String fechaNac = formatoFecha.format(func.getFechaNac());
        String doc=Integer.toString(func.getDocumento());
        String [] ArrayDatosPersona={doc,func.getNombre(),func.getApellido(),func.getEmail(),fechaNac,func.getTelFijo(),func.getCelular() 
                ,func.getEstadoCivil().toString(),func.getDomicilio(),func.getSexo().toString()};
        
        String[] arrayDatosFuncionario={doc,func.getUsuario(),func.getContrasena(),func.getRol().toString()};     
        try {
            logicaAdmin.agregarFuncionario(arrayDatosFuncionario);
            logicaAdmin.agregarFuncionarioPersona(ArrayDatosPersona);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ConfirmacionRegistro.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "Funcionario Agregado con Exito", "", JOptionPane.PLAIN_MESSAGE);
        this.dispose();
    }//GEN-LAST:event_confirmarButtonActionPerformed

