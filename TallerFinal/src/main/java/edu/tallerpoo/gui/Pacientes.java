    /**
     * metodo que actualiza el contenido de la tabla que se muestra por pantalla
     * 
     */
    public void actualizar(){   
        LogicaRegistro r=new LogicaRegistro();
        ArrayList<Registro>registro=new ArrayList();

        registro=r.leerRegistro();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        Utilidades u=new Utilidades();
        for(Registro p : registro){
            if((p.getEstado()==tipoEstado.Atendido)||(p.getEstado()==tipoEstado.Procesado)){
              continue;  
            }else{
            String [] array={Integer.toString(p.getPaciente().getDocumento()),Integer.toString(p.getId()),p.getPaciente().getNombre(),p.getPaciente().getApellido(),p.getMotivo(),Utilidades.convertDate(p.getFecha()),Utilidades.convertHora(p.getHora())};
            model.addRow(array); 
            }
        }
    }
    
