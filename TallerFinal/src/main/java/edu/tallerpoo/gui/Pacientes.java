    /**
     * cuando se preciona el boton "atender" se le asigna el paciente seleccionado al medico y muestra la ventana para asignarle un box al paciente
     * 
     * @param evt 
     */
    private void atenderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atenderButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String[] array = new String[model.getColumnCount()];
        LogicaRegistro r=new LogicaRegistro();
        
        
        
        for(int i=0; i<model.getColumnCount()-1; i++){
            array[i] = model.getValueAt(jTable1.getSelectedRow(), i).toString();
        }
        
        Boxes b = new Boxes();
        b.setData(array, this.medic);
        b.setVisible(true);
        b.setLocationRelativeTo(null);
        actualizar();
    }//GEN-LAST:event_atenderButtonActionPerformed

