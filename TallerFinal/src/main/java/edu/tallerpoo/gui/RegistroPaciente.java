    /**
     * al precionar el boton "Registrar paciente" este metodo genera un paciente con todos sus parametros cargados y lo envia para confirmacion
     * 
     * @param evt {@link java.awt.event.ActionEvent}
     */
    private void RegistrarPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarPacienteActionPerformed
        Paciente p=new Paciente();

        
        String nombre=nombreTextField.getText();
        p.setNombre(nombre);
        
        String apellido=apellidoTextField.getText();
        p.setApellido(apellido);
        
        Date fechaNacimiento = FechaNacimiento.getDate();
        SimpleDateFormat formatoFecha = new SimpleDateFormat("dd-MM-yyyy");
        String FechaString = formatoFecha.format(fechaNacimiento);
        
        
        p.setFechaNac(fechaNacimiento);
        
        String domicilio=domicilioTextField.getText();
        p.setDomicilio(domicilio);
                
        String Documento=documentoTextField.getText();

        
        String TelFijo=telefonofijoTextField.getText();
        p.setTelFijo(TelFijo);
        
        String Celular=celularTextField.getText();
        p.setCelular(Celular);
        
        String EstadoCivil = (String) estadocivilComboBox1.getSelectedItem();
            switch (EstadoCivil){
            case "Casado":
                p.setEstadoCivil(tipoEstCivil.Casado);
                break;
            case "Divorciado":
                p.setEstadoCivil(tipoEstCivil.Divorciado);
                break;
            case "Soltero":
                p.setEstadoCivil(tipoEstCivil.Soltero);
                break;
            case "Viudo":
                p.setEstadoCivil(tipoEstCivil.Viudo);
            }
           
         String tipoSexo=(String)sexoComboBox.getSelectedItem();
         
         String motivoConsulta=MotivoConsulta.getText(); 
        
        String email=emailTextField.getText();
        p.setEmail(email);
        
        String NombrePersonaContacto=nombrePersonaTextField.getText();
        
        String DomicilioPersonaContacto=domicilioPersonaTextField.getText();
        
        String CelularPersonaContacto=celularPersonaTextField1.getText();
        
        String [] ArrayDatos={Documento,nombre,apellido,email,FechaString,TelFijo,Celular,EstadoCivil,domicilio,tipoSexo};
        String []DatosPaciente={Documento,NombrePersonaContacto,DomicilioPersonaContacto,CelularPersonaContacto};
        LogicaRegistro escrituraPaciente=new LogicaRegistro();
        escrituraPaciente.agregarPacientes(ArrayDatos);
        LogicaRegistro escrituraPacienteAlCsvPersonas=new LogicaRegistro();
        escrituraPacienteAlCsvPersonas.agregarPacientes2(DatosPaciente);
        
       
        String [] Persona={Documento, NombrePersonaContacto, DomicilioPersonaContacto, CelularPersonaContacto};   
        LogicaPaciente Escritura=new LogicaPaciente();
        Escritura.escribirCsvRegistroPaciente(Persona);
        
        //Escribe en logicaRegistro el csv Registro con la hora actual que se hizo el registro, motivo y dia
        LogicaRegistro añadirRegistro=new LogicaRegistro();     
        SimpleDateFormat fhora=new SimpleDateFormat("HH:mm");
        Date Hora = new Date();
        SimpleDateFormat fFecha=new SimpleDateFormat("dd-MM-yyyy");
        Date Fecha=new Date();
        String hora=fhora.format(Hora);
        String fecha=fFecha.format(Fecha);  
        añadirRegistro.EscribirRegistro(motivoConsulta,fecha,hora);
        
        
        //--------------------------------------------------------------
        ConsultaPaciente consulta=new ConsultaPaciente();
        consulta.setData(administrativo);
        consulta.setVisible(true);
        consulta.setLocationRelativeTo(null);
        this.dispose();
    }//GEN-LAST:event_RegistrarPacienteActionPerformed

