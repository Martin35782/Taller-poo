    /**
     * Este metodo valida que el texto que se ingreso no contenga catacteres numericos ni comas.
     * 
     * @param txt {@link String}-Texto el cual sera validado
     * 
     * @return {@link Boolean}-True si no contiene ningun numero y/o coma. False en caso contrario
     */
    public boolean validarTxt(String txt){
        boolean res;
        if(txt.isEmpty() || !txt.matches("^[^0-9+,.-]+$")) {
            res = false;
        }else{
            res = true;
    }
        return res;
    }
    
    /**
     * Este metodo valida que el numero que se ingreso no contenga letras y/o comas.
     * 
     * @param num {@link String}-String el cual sera validado
     * 
     * @return {@link Boolean}-True si no contiene ninguna letra y/o coma. False en caso contrario
     */
    public boolean validarNum(String num){
        boolean res;
        if(num.isEmpty() || !num.matches("^[^A-Za-z+,.-]+$")) {
            res = false;
        }else{
            res = true;
        }
        return res;
    }
        
    /**
     *  Este metodo valida si todos los parametros ingresados por teclado son validos
     *  en caso de serlos habilita el boton para registrar el funcionario
     *
     */
    public void validar() {
    boolean domicilioValido,emailValido,fechaValido,direcpcontValido=false;
    // Validación del campo nombre
    
    boolean nombreValido = validarTxt(nombreTextField.getText());

    boolean apellidoValido = validarTxt(apellidoTextField.getText());
    
    if(domicilioTextField.getText().isEmpty()) {
        domicilioValido = false;
    }else{
        domicilioValido = true;
    }
    
    boolean documentoValido = validarNum(documentoTextField.getText());
    
    boolean celularValido = validarNum(celularTextField.getText());
    
    
    if(emailTextField.getText().isEmpty() || !emailTextField.getText().matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$")) {
        emailValido = false;
    }else{
        emailValido = true;
    }

    if(FechaNacimiento.equals("")) {
        fechaValido = false;
    }else{
        fechaValido = true;
    }
    
    
    boolean nombrepcontValido=validarTxt(nombrePersonaTextField.getText());
    
    if(domicilioPersonaTextField.getText().isEmpty()) {
        direcpcontValido = false;
    }else{
        direcpcontValido = true;
    }

    boolean celupcontValido=validarNum(celularPersonaTextField1.getText());
 
    
    
    RegistrarPaciente.setEnabled(nombreValido && apellidoValido && domicilioValido && documentoValido && celularValido && emailValido && fechaValido && nombrepcontValido && direcpcontValido && celupcontValido );
}

    
 
