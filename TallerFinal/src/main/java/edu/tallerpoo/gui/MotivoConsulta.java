    /**
     * Este metodo valida que el campo del motivo de la consulta no este vacio.
     */
    public void validar() {
     if(motivoPane.getText().isEmpty()) {
        confirmar.setEnabled(false);
     }else{
        confirmar.setEnabled(true);
        }
    }      
    
    /**
     * cuando se preciona el boton "confirmar" se a√±ade el motivo de consulta del paciente
     * 
     * @param evt 
     */
    private void confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarActionPerformed
        
        JOptionPane.showMessageDialog(this, "Registro Finalizado", "", JOptionPane.INFORMATION_MESSAGE);
        ConsultaPaciente consulta=new ConsultaPaciente();
        consulta.setVisible(true);
        consulta.setLocationRelativeTo(null);
        this.dispose();
        LogicaRegistro reg=new LogicaRegistro();
        Date fecha=new Date();
        
        
        
        reg.EscribirRegistroPacienteExistente(this.dni, motivoPane.getText(), Utilidades.convertDate(fecha), Utilidades.convertHora(fecha));
        
        
    }//GEN-LAST:event_confirmarActionPerformed

    /**
     * este metodo llama al procedimiento validar() cada vez que se preciona una tecla en el campo de texto del motivo de 
     * la consulta
     * 
     * @param evt 
     */
    private void motivoPaneKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_motivoPaneKeyReleased
        validar();
    }//GEN-LAST:event_motivoPaneKeyReleased

