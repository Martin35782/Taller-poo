    private void TablaPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_TablaPropertyChange
 
        try{
            String a=Niveles.getSelectedItem().toString();
            switch (a){
                    case "1":
                        colorBarra.setBackground(Color.red);
                        Tabla.setValueAt("ROJO",0,1);
                        Tabla.setValueAt("RIESGO VITAL INMEDIATO",0,2);
                        Tabla.setValueAt("ATENCION INMEDIATA",0,3);
                        break;
                    case "2":
                        colorBarra.setBackground(Color.orange);
                        Tabla.setValueAt("NARANJA",0,1);
                        Tabla.setValueAt("MUY URGENTE",0,2);
                        Tabla.setValueAt("10-15 MIN.",0,3);
                        break;
                    case "3":
                        colorBarra.setBackground(Color.yellow);
                        Tabla.setValueAt("AMARILLO",0,1);
                        Tabla.setValueAt("URGENTE",0,2);
                        Tabla.setValueAt("60 MIN.",0,3);
                        break;
                    case "4":
                        colorBarra.setBackground(Color.green);
                        Tabla.setValueAt("VERDE",0,1);
                        Tabla.setValueAt("NORMAL",0,2);
                        Tabla.setValueAt("2 HORAS",0,3);
                        break;
                    case "5":
                        colorBarra.setBackground(Color.blue);
                        Tabla.setValueAt("AZUL",0,1);
                        Tabla.setValueAt("NO URGENTE",0,2);
                        Tabla.setValueAt("4 HORAS",0,3);
                        break;
              }
            }
            catch (Exception e){
                    System.out.println("anashe");
                    }
   
    }//GEN-LAST:event_TablaPropertyChange

